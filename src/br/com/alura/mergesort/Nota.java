package br.com.alura.mergesort;

public class Nota {

    private String aluno;
    private double valor;

    public Nota(String aluno, double valor) {
        this.aluno = aluno;
        this.valor = valor;
    }

    public double getValor() {
        return this.valor;
    }

    public String getAluno() {
        return this.aluno;
    }

}
